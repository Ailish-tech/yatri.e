// Função utilitária para obter o código de um país
export const getCountryCode = (countryName: string): string => {
  return countryCodes[countryName] || "";
};

// Função utilitária para obter o nome do país pelo código
export const getCountryName = (countryCode: string): string => {
  const entry = Object.entries(countryCodes).find(([_, code]) => code === countryCode);
  return entry ? entry[0] : "";
};
